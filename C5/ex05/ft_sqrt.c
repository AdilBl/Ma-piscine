int	ft_sqrt(int nb)
{
	int	i;
	int	n;

	if (nb == 0)
		return (0);
	n = 0;
	i = 0;
	while (n < nb && i < 46341)
	{
		n = i * i;
		i++;
	}
	if (n == nb)
		return (i - 1);
	return (0);
}
