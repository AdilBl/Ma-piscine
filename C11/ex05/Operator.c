int	add(int a, int b)
{
	return (a + b);
}

int	sous(int a, int b)
{
	return (a - b);
}

int	div(int a, int b)
{
	return (a / b);
}

int	multi(int a, int b)
{
	return (a * b);
}

int	modu(int a, int b)
{
	return (a % b);
}
