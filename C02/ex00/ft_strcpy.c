char	*ft_strcpy(char *dest, char *src)
{
	int	cpt;

	cpt = 0;
	while (src[cpt] != '\0')
	{
		dest[cpt] = src[cpt];
		cpt ++;
	}
	dest[cpt] = '\0';
	return (dest);
}
